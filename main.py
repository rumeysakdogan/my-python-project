# Data Types
print("triangle")  # str
print(2)  # int
print(2.5)  # float
print(-10 < 0)  # bool (True/False)
print("20 days are " + str(50) + " minutes")  # string concatenation
print(f"20 days are {20 * 24 * 60} minutes")  # format string
print(20 * 24 * 60)  # how many minutes in 20 days

# variables
